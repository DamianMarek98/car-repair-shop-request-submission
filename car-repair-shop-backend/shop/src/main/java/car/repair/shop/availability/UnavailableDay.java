package car.repair.shop.availability;

import car.repair.shop.commons.dynamodb.converter.LocalDateConverter;
import com.amazonaws.services.dynamodbv2.datamodeling.*;
import lombok.AccessLevel;
import lombok.Getter;
import lombok.Setter;

import java.time.LocalDate;

@Getter(AccessLevel.PUBLIC)
@Setter(AccessLevel.PUBLIC)
@DynamoDBTable(tableName = "unavailable_day")
public class UnavailableDay {
    @DynamoDBAttribute(attributeName = "unavailable_date")
    @DynamoDBTypeConverted(converter = LocalDateConverter.class)
    LocalDate date;
    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String id;
}
